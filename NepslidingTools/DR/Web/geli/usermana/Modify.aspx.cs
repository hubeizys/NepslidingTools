using System;
using System.Data;
using System.Configuration;
using System.Collections;
using System.Web;
using System.Web.Security;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Web.UI.WebControls.WebParts;
using System.Web.UI.HtmlControls;
using System.Text;
using Maticsoft.Common;
using LTP.Accounts.Bus;
namespace Maticsoft.Web.geli.usermana
{
    public partial class Modify : Page
    {       

        		protected void Page_Load(object sender, EventArgs e)
		{
			if (!Page.IsPostBack)
			{
				if (Request.Params["id"] != null && Request.Params["id"].Trim() != "")
				{
					int ID=(Convert.ToInt32(Request.Params["id"]));
					ShowInfo(ID);
				}
			}
		}
			
	private void ShowInfo(int ID)
	{
		Maticsoft.BLL.geli.usermana bll=new Maticsoft.BLL.geli.usermana();
		Maticsoft.Model.geli.usermana model=bll.GetModel(ID);
		this.lblID.Text=model.ID.ToString();
		this.txtusername.Text=model.username;
		this.txtaddtime.Text=model.addtime.ToString();
		this.txtpassword.Text=model.password;
		this.txtpower.Text=model.power;

	}

		public void btnSave_Click(object sender, EventArgs e)
		{
			
			string strErr="";
			if(this.txtusername.Text.Trim().Length==0)
			{
				strErr+="username不能为空！\\n";	
			}
			if(!PageValidate.IsDateTime(txtaddtime.Text))
			{
				strErr+="addtime格式错误！\\n";	
			}
			if(this.txtpassword.Text.Trim().Length==0)
			{
				strErr+="password不能为空！\\n";	
			}
			if(this.txtpower.Text.Trim().Length==0)
			{
				strErr+="power不能为空！\\n";	
			}

			if(strErr!="")
			{
				MessageBox.Show(this,strErr);
				return;
			}
			int ID=int.Parse(this.lblID.Text);
			string username=this.txtusername.Text;
			DateTime addtime=DateTime.Parse(this.txtaddtime.Text);
			string password=this.txtpassword.Text;
			string power=this.txtpower.Text;


			Maticsoft.Model.geli.usermana model=new Maticsoft.Model.geli.usermana();
			model.ID=ID;
			model.username=username;
			model.addtime=addtime;
			model.password=password;
			model.power=power;

			Maticsoft.BLL.geli.usermana bll=new Maticsoft.BLL.geli.usermana();
			bll.Update(model);
			Maticsoft.Common.MessageBox.ShowAndRedirect(this,"保存成功！","list.aspx");

		}


        public void btnCancle_Click(object sender, EventArgs e)
        {
            Response.Redirect("list.aspx");
        }
    }
}
